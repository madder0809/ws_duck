<h1>鸭脖起源</h1>
<p>鸭脖最早起源于武汉精武路，当时的人们吃鸭的时候，鸭头，鸭爪，鸭脖等鸭附件都是直接被扔掉的。而精明的武汉人觉得这是一种浪费。于是加入花椒，辣椒等多种香草料进行精心卤制，并形成了最初的红遍全国的精武鸭脖。而随着时光的流逝，大众口味的变更。于是在精武鸭脖的基础上又演变升级出了更多不同特色的鸭脖系列，而武汉鸭脖更是其中的典范，它在保留精武鸭脖的同时，又融入了更多的元素，让之前的鸭脖变口味变得更加的丰富多彩，口感更加富有层次，在全国掀起了一股这麻辣风。</p>
<p>取上二者之意，故解释为：绝妙的味道、绝无仅有的味道。其经典美味鸭脖、鸭架、藕片、毛豆等产品深得消费者青睐。它入口鲜、香、麻、辣，丝丝透味，四种口感让人越吃越有味，一波又一波的味蕾享受让消费者的休闲生活无比愉悦和满足。这便是&ldquo;够味才痛快&rdquo;的绝妙美食体验。<br />
